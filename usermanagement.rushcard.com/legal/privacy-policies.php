                        <h3>INFORMATION COLLECTION</h3>

                        <h4>Information You Provide</h4>
                        <p>We collect personal information from you when you choose to share it with us. This may include when you access or use our websites or mobile applications, create an online account with us, request information from us, sign up for our newsletters or our email lists, participate in a promotion, or otherwise contact us. The personal information we collect may include your name, address, email address, phone number, date of birth, information about your payment at a retail location, information contained on a check that you upload for remote deposit, financial account information used for payments, loads or direct deposits, and any other information you choose to provide.</p>
                        <p>We also collect your information via third-party social networks when you use them to connect with us (for example, by posting comments on our posts or sending messages to us). If you choose to connect with us on third-party social networks, we may have access to certain information about you from that social network, such as your profile name, profile picture, networks, username and user ID, and comments, questions, and other posted content based on your privacy settings on that social network.</p>
                        <h4>Information Automatically Collected</h4>
                        <p>
                            We may use cookies, web beacons, pixel tags, log files, local shared objects (Flash cookies), and other technologies for security and authentication purposes, to collect certain information when you visit and use our websites or mobile applications, to deliver online advertisements that we think would be more relevant to your interests, to monitor interactions with our emails and online advertisements, and to allow us to keep track of analytics and certain statistical information that enable us to improve our websites, mobile applications, products, and services.
                        </p>
                        <p>
                            For example, we may automatically collect certain non-personal information from you, including, but not limited to, your browser type and version, device type, Flash version, operating system, Internet Protocol ("<strong>IP</strong>") address, unique device identifier of your computer(s) or device(s) that are used to access our websites or mobile applications, software version, and the domain name from which you accessed our websites or mobile applications. We may also collect information about your browsing and use behavior, such as the date and time you visit our websites or use our mobile applications, the areas or pages of the websites or mobile applications that you visit, the amount of time you spend viewing or using the websites or mobile applications, the number of times you return to the websites or mobile applications, other click-stream or website or mobile application usage data, emails that you open, forward, or click-through to our websites, other websites that you may visit, and your activity on our websites and mobile applications.
                        </p>
                        <p>If you use certain features of our websites or mobile applications that involve location-based services (such as the ATM locator), we may also collect location information in order to: (1) provide you with the services or products that you request, including access to location-based information, and (2) monitor for fraud or other suspicious activity. The ways in which we collect location information, and the type of location information we collect, depend on the devices you use to access our websites or mobile applications and the settings for those devices. You may also manually enter a location (such as address, city, and postal code) to use the location-based services of our websites and mobile applications.</p>
                        <p>You can stop sharing location information with us at any time through your device or web browser settings or by deleting the mobile application from your device. If you stop sharing your location, certain features of our websites and mobile applications may not function properly or at all. If you have specific questions about the privacy settings of your device, we suggest you contact the manufacturer of your device, your mobile service provider, or web browser provider for help.</p>
