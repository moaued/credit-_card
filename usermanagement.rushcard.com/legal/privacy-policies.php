                        <p>We and our network advertisers may periodically show you online ads for products and services you might be interested based on your visits to both our websites and other websites (a process known as "behavioral advertising"). In order to do this, we need to place a cookie or other tracking device on your device. If you prefer to not receive online ads in this manner, you can opt out of some network advertising programs that use your information by tracking your browsing habits via cookies or other tracking devices.</p>
                        <p>To learn more about the use of cookies and other technologies to deliver more relevant advertising, and to learn how to opt out of it in desktop and mobile browsers on the particular device on which you are accessing this Privacy Statement, please visit <a href="http://optout.aboutads.info/#/">http://optout.aboutads.info/#/</a> and <a href="http://optout.networkadvertising.org/#/">http://optout.networkadvertising.org/#/</a>. You may download the AppChoices app at <a href="https://www.aboutads.info/appchoices">www.aboutads.info/appchoices</a> to opt out in mobile applications. Opting out of a network does not mean you will no longer receive online advertising. It only means that the network from which you opted out will no longer deliver ads tailored to your web preferences and usage patterns.</p>

